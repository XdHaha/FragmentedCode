
public class LinkList {

	private ListNode mHead;
	private ListNode mTail;

	public LinkList() {
	}

	public void setHead(ListNode head) {
		mHead = head;
	}

	public void setTail(ListNode tail) {
		mTail = tail;
	}
	
	public ListNode getHead() {
		return mHead;
	}
	
	public ListNode getTail() {
		return mTail;
	}	

}
